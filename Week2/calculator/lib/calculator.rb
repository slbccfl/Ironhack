class Calculator

	def addition(first_number, second_number)
		first_number + second_number
	end

	def subtraction(first_number, second_number)
		first_number - second_number
	end

	def multiplication(first_number, second_number)
		first_number * second_number
	end

	def division(first_number, second_number)
		first_number / second_number
	end
end